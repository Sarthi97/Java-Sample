public class Q1 {
    public static void main(String[] args) {
        int n = 10; // You can change this value to print more or fewer natural numbers
        printNaturalNumbers(n);
    }
    public static void printNaturalNumbers(int n) {
        for (int i = 1; i <= n; i++) {
            System.out.println(i);
        }
    }
    
}
